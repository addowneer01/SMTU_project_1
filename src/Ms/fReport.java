package Ms;

import Bd.Document;
import Bd.Project;

public class fReport extends Message{
    public fReport(int idC, String namePersonalC, String nameProjectC, int idDocumentC, int idReportC, String commentC) {
        super(idC, namePersonalC, nameProjectC, idDocumentC, TYPE_REPORT);
        idReport=idReportC;
        comment=commentC;
    }
    public fReport(int idC, Project project, Document document, int idReportC, String commentC) {
        super(idC, project.namePersonal, project.name, document.id, TYPE_REPORT);
        idReport=idReportC;
        comment = commentC;
    }
    public int idReport;
    public String comment;
}
