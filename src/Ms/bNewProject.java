package Ms;

import Bd.Document;
import Bd.Project;

public class bNewProject extends Message{
    public bNewProject(int idC, String namePersonalC, String nameProjectC, int idDocumentC, String nameC, String commentC) {
        super(idC, namePersonalC, nameProjectC, idDocumentC, TYPE_NEW_PROJECT);
        name=nameC;
        comment=commentC;
    }
    public bNewProject(int idC, Project project, Document document, String nameC, String commentC) {
        super(idC, project.namePersonal, project.name, document.id, TYPE_NEW_PROJECT);
        comment = commentC;
        name=nameC;
    }
    public String name;
    public String comment;
}
